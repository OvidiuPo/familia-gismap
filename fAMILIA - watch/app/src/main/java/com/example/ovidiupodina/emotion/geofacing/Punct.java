package com.example.ovidiupodina.emotion.geofacing;

public class Punct {

    public Punct(double x, double y) {
        this.x = x;
        this.y = y;
    }

    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
